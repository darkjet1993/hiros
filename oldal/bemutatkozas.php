<h1>Bemutatkozás</h1>
<br>
<br>
<p><b>Hírös Sport Nonprofit Kft. a Kecskeméti Sport Kht. jogutódjaként jött létre 2008. augusztus 11-én a közhasznú szervezeti forma megszűnése miatt.
 Az új társaság, elődje tevékenységét folytatva Kecskemét Megyei jogú város tulajdonában lévő létesítmények üzemeltetése és hasznosítása mellett
 létrehozott egy különálló divíziót, a Sportiskolát.</b></p>

<p>A Kecskeméti Sport Kht. az alapító szándéka szerint a jogutód nélkül megszűnt Sportigazgatóság által üzemeltetett sportvagyon további működtetésére jött létre.
 Ez a vagyon az átadás időpontjában leromlott, leamortizálódott, szinte működésképtelen,
 hasznosíthatatlan állapotban volt a használó egyesületek jogosultságainak és kötelezettségeinek megfelelő módon történő rendezése nélkül.</p>

<p>A Sportigazgatóság továbbfoglalkoztatásukat vállaló munkatársaival kezdte meg és túlnyomó részben jelenleg is velük folytatja tevékenységét a Kht.
 az ellátandó feladatok által megkövetelt változtatásokkal.</p>

<p>A társaság életében az alapítással együtt járó feladatokon túl meghatározó volt a Széktói Stadion rekonstrukciója (2001-2002),
 és kiemelten a Széktói Strand felújítása (2002-2003), mely beruházás során a Kht.-ra komoly és felelősségteljes feladat hárult annak sikeres lebonyolítása érdekében,
 melyet szintén eredményesen teljesített a társaság.</p>
 
</p>A beruházások során létrejött vagyon üzemeltetése több ezer négyzetméterrel (pl. 1300 m2-es fejépület a stadionban,
 több mint 3300 m2 vízfelület a strandon) és természetesen új feladatokkal, nagyobb létszámigénnyel és magasabb üzemeltetési költségekkel jár,
 melyeket az arányokhoz képest szintén minimális növekménnyel teljesít a társaság.</p>

<p>Mindezeken túl számos kisebb-nagyobb fejlesztést hajtunk végre folyamatosan, rendezvények sorát szervezzük úgy,
 hogy a társaság állandó munkavállalói létszáma mind összetételében, mind számában csak lépésről-lépésre változik az új feladatok szükségszerű ellátásának megfelelően.
A Hírös Sport Nonprofit Kft. működésén belül elkülönül a létesítményüzemeltetés,
 a Kecskeméti Sportiskola és a Mercedes-Benz Kosrálabda Akadémia működtetése. A divíziók a társaság keretein belül,
 de önállóan, saját költségvetésből gazdálkodnak.
</p>



